        bottom = c["bl"] + e["h"] * width + c["br"]
        
        body = []
        for line in lines:
            padded = line.ljust(width)
            body.append(f"{e['v']}{padded}{e['v']}")
            
        return "\n".join([top] + body + [bottom])


