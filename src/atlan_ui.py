class GlitchEffect:
    @staticmethod
    def glitch_text(text: str, intensity: float = 0.1) -> str:
        result = ""
        for char in text:
            if random.random() < intensity:
                glitch_chars = "в–“в–’в–‘в–Ҳв–“в–’в–‘"
                result += random.choice(glitch_chars)
            else:
                result += char
        return result
    
    @staticmethod
    def scan_lines(text: str, lines: int = 5) -> List[str]:
        output = []
        for _ in range(lines):
            offset = random.randint(-2, 2)
            if offset != 0:
                glitch = "в–“" * abs(offset)
                output.append(glitch + text)
            else:
                output.append(text)
        return output
    
    @staticmethod
    def corrupt(data: str, corruption: float = 0.05) -> str:
        result = ""
        for char in data:
            if char == "\n":
                result += "\n"
            elif random.random() < corruption:
                result += random.choice("в–“в–’в–‘в–Ҳв– в–Ў")
            else:
                result += char
        return result


